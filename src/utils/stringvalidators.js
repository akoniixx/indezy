export const email = str => /^\w+([.-]?\w+)*@\w+([.-]?\w+)*(\.\w{2,3})+$/.test(str);

/**
 * Avaiable formats
 * '0819543213', '081-954-3213', '081-9543213', '+66819543213',
 * '+6681-954-3213', '+6681-9543213', '(+66)819543213',
 * '022868808', '02-286-8808', '02-2868808', '(+66)22868808',
 * '+6622868808', '+662-286-8808', '+662-2868808', '(+66)22868808'
 */
export const tel = str => /^\(?\+?([0-9]{2,3})?\)?([0-9]{1,2})?[-. ]?([0-9]{3})[-. ]?([0-9]{4})?$/.test(str) && str.length >= 9;